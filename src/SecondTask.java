public class SecondTask {
    public static void main(String[] args) {

        int a = 10;
        int b = 10;

        // Equal to operator
        boolean c = (a == b);
        System.out.println("a == b is " + c);

        // Not equal to operator
        c = (a != b);
        System.out.println("a != b is " + c);

        // Less than operator
        c = (a < b);
        System.out.println("a < b is " + c);

        // Greater than operator
        c = (a > b);
        System.out.println("a > b is " + c);



    }
}
